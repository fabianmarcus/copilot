/**
 * Beschreibt kurz, was die zentrale Funktion macht.
 * @param input Beschreibt den Parameter.
 * @returns Beschreibt den Rueckgabewert.
 */
export function centralFunctionName(input: string): string {
  return formatOutput(input);
}

export default centralFunctionName;

